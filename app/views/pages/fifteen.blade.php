<p>
    香港作家西西，在成名作“像我这样的一个女子”里，讲述70年代，一个专职为遗体化妆的年轻女子。因为长期在阴暗的地方工作， 肤色苍白。她的脸容朴素，常着素白衣服，为了方便出入工作的地方。她初识的男朋友，喜欢她这个样子，以为她的工作是为新娘化妆，却不知道，“他一直以为是我为他而洒的香水，其实不过是附在我身体上的防腐剂的气味吧了”。在她准备向他表白的那个约会，男友带来一束巨大的花朵，非常非常地美丽。他很快乐，她却很悲伤，因为，在她们的行业中，花朵，就是诀别的意思。
</p>
<p>
    文章最后没有揭晓，她说出真相之后，男友有没有接受她。四十年后的今天，他会吗？我们会吗？
</p>
<p>
    我们畏惧死亡，所以害怕殡仪人员。这些人，在焚化或下葬前的最后几天，把遗体打理得体体面面。假如我们不懂得尊敬他们，那么，在最后的路途上，遗体躺在三面透风的货车里，车子停在交通灯前，距离旁边作舌的摩托骑士，不到三尺。这怨得了谁？
</p>
<p>
    而，像我这样的一个琴师，在遗体经过我身后之时，我伫立静默；在病人还有气息之时，以恭敬的琴声相陪，如此而已。
</p>
<p>
    那对母子，在一个炎热的午后，躲在阴凉的角落，享受彼此的体温。他们找寻一些特殊的广告，沉浸在简单的快乐之中。他们哈哈大笑，翻过了一页又一页，好像永无止境。
</p>