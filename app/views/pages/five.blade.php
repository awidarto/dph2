    <p>
        我带她坐在钢琴旁的沙发上，倒回去弹琴。她架着太阳眼镜，正襟危坐，好像小学生惹了祸，被叫进校长室。我寻思如何叫她放轻松点。这时，画家病人摆了一个指挥乐队的架势，另一人吹起口哨伴和琴声。几个小孩，叽里呱啦，打着手机游戏走过。大概是这些来来去去的澎湃生命力影响了她吧？她露出笑容，摘下太阳眼镜。我说：我刚认识这位画家，我们这里都是相逢恨晚。她说：我唯一接触过的临终病人是我爸，他病了很久，等我回去。他好瘦，好干。她顿了一会，又说：当时把我哥哥弄得好累，假如大陆家乡也有这样的设施，那该有多好。然后，她说：可以走走看看吗？我带她绕了一圈，稍作解释。病人家属看见我胸前挂着的义工牌子，都微笑点头示意。
    </p>
    <p>
        她走后，给我发了一则短信，写道：还是蛮感激你的，至少，跨出从未走过也不认为我自己会做的一件事。今天去，也是错误的以为那是老人院之类的。可能是钢琴的错觉吧，要不，也不会那么可笑的带那些东西去了，还瞎忙一通。我回：你瞎忙是因为心地好。每个人都得选择自己的路。
    </p>
    <p>
        不是吗？每道生命最后都会到达终点。我们不能决定时间，大概也无法选择尊严的离去。然而，春节每年都会来的。可以的话，与能够互送温暖欢乐的人们，把酒喝光，把薯片豆豆都吃掉，开开心心地过吧。这几年，我选对了两项义工：一是在托福院弹琴，二是协助议员聆听和解决居民的疑难杂事。前者让我体会我们无力控制生命，后者让我明白人生麻烦痛苦气愤事，许多皆由心生。从此我只专注我能力所及可以做好之事。
    </p>
    <p>
        六时许，雨停了。在回家的途中，我顺道在地铁站接了隽敏。十七年前，我从医院把她抱回家。当时，她是一个健康的婴儿，裹在粉红色的包包里，小小，暖暖。今天，她在同学家温完了课，等着回家吃年夜饭。我把车靠在路旁，她开门上车，长发飘逸如一阵春风，像她妈妈一样美丽。
    </p>
