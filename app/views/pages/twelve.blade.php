        我弹时，旁边坐着一名大约十岁的小男孩，在平板上打游戏。一会儿，他哥哥和妈妈从病房中走出来。哥哥转过身，拥抱妈妈。哥哥差不多和妈妈一样高，头埋在妈妈的肩膀上，背部微微颤栗。小男孩看着哥哥，游戏也不打了。
