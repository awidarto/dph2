    <p>
        时间终于来到。老妇人蹒跚步入慈怀中心，一拐一拐，最后一次走到丈夫的床前。她一生未染铅华，没受过高深教育，但她知道怎么做，也做了一世。 那日，所有人一起站立，静默无声。我的琴声，也戛然而止。家后，是她尊贵的华冠。
    </p>
    <p>
        你身旁有位家后吗？或许你就是那个？回去，记得把他的手牵条条，不管人情世事，你是否已经看透。
    </p>